package br.com.etec.lancheira;

public class FoodItem {
    private int id;
    private String nome;
    private String descricao;
    private String imagem_url;

    public int getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public String getDescricao() {
        return descricao;
    }

    public String getImagemUrl() {
        return imagem_url;
    }
}

